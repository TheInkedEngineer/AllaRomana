import UIKit

/// A Namespace containing all the colors used in the app
enum Palette {
  static var white: UIColor {
    return UIColor(displayP3Red: 1, green: 1, blue: 1, alpha: 1)
  }

  static var lightGrey: UIColor {
    return UIColor(displayP3Red: 0.60784314, green: 0.60784314, blue: 0.60784314, alpha: 1)
  }

  static var darkGrey: UIColor {
    return UIColor(displayP3Red: 0.29411765, green: 0.29411765, blue: 0.29411765, alpha: 1)
  }

  static var black: UIColor {
    return UIColor(displayP3Red: 0.14901961, green: 0.14901961, blue: 0.14901961, alpha: 1)
  }

  static var yellow: UIColor {
    return UIColor(displayP3Red: 1, green: 1, blue: 0, alpha: 1)
  }
}
